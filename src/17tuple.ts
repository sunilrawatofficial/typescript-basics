//tuple is similar to array, but we can define the type of specific every element in tuple.
let array = [1, "Sachin", 2, "Virat", false]
console.log("Normal Array", array)


let tuple:[string, number, string, number, boolean] = [
    "Sachin", 1, "Virat", 2, true,
]
console.log("Tuple Array", tuple)
